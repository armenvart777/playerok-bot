@router.message(Command("start"))
async def cmd_start(message: Message):
    await message.answer(WELCOME_TEXT.strip(), reply_markup=main_menu_kb())


@router.message(Command("help"))
async def cmd_help(message: Message):
    await message.answer(HELP_TEXT.strip())


@router.message(Command("setup"))
async def cmd_setup(message: Message):
    await message.answer(SETUP_GUIDE_TEXT.strip(), disable_web_page_preview=True)


@router.callback_query(MenuCB.filter(F.action == "menu"))
async def cb_menu(callback: CallbackQuery):
    await callback.message.edit_text(WELCOME_TEXT.strip(), reply_markup=main_menu_kb())
    await callback.answer()
